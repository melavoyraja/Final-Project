    public void populateSensorData() {
        DefaultTableModel model = (DefaultTableModel) sensorDataTable.getModel();
        model.setRowCount(0);

        sensor.getSensorDataDirectory().getSensorDataList().stream().filter((wqsd) -> (wqsd instanceof WaterQualitySensorData)).map((wqsd) -> {
            Object[] row = new Object[3];
            row[0] = wqsd;
            row[1] = wqsd.getDataRecordedDate();
            row[2] = ((WaterQualitySensorData) wqsd).getLevelOfConcern();
            return row;
        }).forEach((row) -> {
            model.addRow(row);
        });

    }

    public String readImage() {
        BufferedImage img;
        String image = null;
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        try {
            img = ImageIO.read(new File("noimage.jpg"));
            ImageIO.write(img, "jpg", baos);
            baos.flush();
            image = Base64.encode(baos.toByteArray());
            baos.close();
        } catch (IOException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        return image;
    }

