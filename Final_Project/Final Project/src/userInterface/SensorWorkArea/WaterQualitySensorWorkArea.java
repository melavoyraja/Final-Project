    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int randomWQI = 0;
        if (random < 6) {
            randomWQI = sensor.randomWQI(true);            
            random++;
            if (random == 5) {
                random = 0;
                randomWQI = sensor.randomWQI(false);                
            }
        }
        
        
        SensorData wqi = sensor.getSensorDataDirectory().addSensorData(Sensor.SensorType.Water);
        ((WaterQualitySensorData) wqi).setWaterQualityIndex(randomWQI);
        String levelOfWQI = ((WaterQualitySensorData) wqi).getLevelOfConcern();
        if (levelOfWQI.equals("Poor") || levelOfWQI.equals("Marginal")) {
            WorkRequest wwwr = new WaterWorksWorkRequest();
            waterWorksOrganization.getWorkQueue().getWorkRequestList().add(wwwr);
            organization.getWorkQueue().getWorkRequestList().add(wwwr);
            wwwr.setMessage(levelOfWQI);
            wwwr.setRequestDate(new Date());
            wwwr.setStatus("Sent");
//            wwwr.setHouse(sensor.getHouse());
            ((WaterWorksWorkRequest) wwwr).setImage(image);
            ((WaterWorksWorkRequest) wwwr).setType(WorkRequest.RequestType.Sensor);
            ((WaterWorksWorkRequest) wwwr).setOrganization(organization);
            wwwr.setSensorData(wqi);
            wwwr.setSensor(sensor);

        }
        wqiJText.setText(String.valueOf(randomWQI));
        populateSensorData();
    }//GEN-LAST:event_jButton1ActionPerformed

