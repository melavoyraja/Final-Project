        int selectedRow = notificationJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from table.", "Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Notification n = (Notification) notificationJTable.getValueAt(selectedRow, 0);
        account.getNotificationList().remove(n);
        loadTable();
