    public void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) notificationJTable.getModel();
        dtm.setRowCount(0);

        for (Notification n : account.getNotificationList()) {
            Object[] row = new Object[2];
            row[0] = n;
            row[1] = n.getMessage();
            dtm.addRow(row);
        }
    }

