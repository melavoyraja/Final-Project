    public void loadCommunityTable() {
        DefaultTableModel model = (DefaultTableModel) communityJTable.getModel();
        model.setRowCount(0);

        for (Community c : communityDirectory.getCommunityList()) {
            Object[] row = new Object[2];
            row[0] = c.getId();
            row[1] = c.getCommunityName();

            model.addRow(row);

        }

    }

    public void loadHouseTable() {
        DefaultTableModel model = (DefaultTableModel) houseJTable.getModel();
        model.setRowCount(0);

        for (Community c : communityDirectory.getCommunityList()) {
            for (House h : c.getHouseDirectory().getHouseList()) {
                Object[] row = new Object[4];
                row[0] = h.getId();
                row[1] = c.getCommunityName();
                row[2] = h.getHouseNo();
                row[3] = h.getStreetName();

                model.addRow(row);
            }
        }
    }

