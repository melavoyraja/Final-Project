    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (checkBlankInput() == Boolean.TRUE) {
            String communityName = communityJText.getText();
            Community community = communityDirectory.addCommunity();
            community.setCommunityName(communityName);
            communityJText.setText("");
            JOptionPane.showMessageDialog(this,
                    "Community created successfully!",
                    "Success!",
                    JOptionPane.INFORMATION_MESSAGE);
            loadCommunityTable();
        } else {
            JOptionPane.showMessageDialog(this, "Please enter all values", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private Boolean checkBlankInput() {
        if (communityJText.getText().length() == 0) {
            return false;
        } else {
            return true;
        }
    }

