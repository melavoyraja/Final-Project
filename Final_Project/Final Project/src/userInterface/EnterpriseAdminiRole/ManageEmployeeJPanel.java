    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        if (checkBlankInput() == Boolean.TRUE) {
            int selectedRow = organizationListJTable.getSelectedRow();

            if (selectedRow < 0) {
                JOptionPane.showMessageDialog(this,
                        "Please select an organization form the organization list!",
                        "Message!",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }
            Organization organization = (Organization) organizationListJTable.getValueAt(selectedRow, 0);
            // Organization organization = (Organization) organizationEmpJComboBox.getSelectedItem();
            String name = nameJTextField.getText();
            int age = Integer.parseInt(ageJText.getText());
            Employee e = organization.getEmployeeDirectory().createEmployee(name);
            e.setAge(age);
            JOptionPane.showMessageDialog(this, "Employee created!", "Info!", JOptionPane.INFORMATION_MESSAGE);
            nameJTextField.setText("");
            ageJText.setText("");
            populateOrganizationComboBox();
        } else {
            JOptionPane.showMessageDialog(this, "Please enter all values", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addJButtonActionPerformed

    private Boolean checkBlankInput() {
        if (nameJTextField.getText().length() == 0
                || ageJText.getText().length() == 0) {
            return false;
        } else {
            return true;
        }
    }
    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed

        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backJButtonActionPerformed

