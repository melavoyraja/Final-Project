    public void populateOrganizationList() {
        DefaultTableModel model = (DefaultTableModel) organizationListJTable.getModel();
        model.setRowCount(0);

        for (Organization organization : organizationDir.getOrganizationList()) {
            Object[] row = new Object[3];
            row[0] = organization;
            row[1] = organization.getOrganizationID();
            if (organization instanceof HouseholdOrganization) {
                row[2] = organization.getCommunity();
            } else {
                row[2] = null;
            }
            model.addRow(row);
        }

    }

    public void populateOrganizationComboBox() {
        organizationJComboBox.removeAllItems();

        for (Organization organization : organizationDir.getOrganizationList()) {
            organizationJComboBox.addItem(organization);
        }
    }

